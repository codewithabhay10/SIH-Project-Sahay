export function getTranslation(lang: string, path: string): string {
  const keys = path.split('.');
  let result: any = translations[lang as keyof typeof translations] || translations.en;
  
  for (const key of keys) {
    result = result?.[key];
    if (result === undefined) {
      // Fallback to English
      result = translations.en;
      for (const k of keys) {
        result = result?.[k];
      }
      break;
    }
  }
  
  return result || path;
}
